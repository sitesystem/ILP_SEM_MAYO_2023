# Calcular el promedio de 3 calificaciones

# ENTRADA DE DATOS
# Declaración de variables
calificación_1 = float(input("Escribe la 1ra calificación: "))
calificacion_2 = float(input("Escribe la 2da calificación: "))
calificacion_3 = float(input("Escribe la 3ra calificación: "))

# PROCESOS (Cálculos u operaciones Matemáticas y/o Lógicas)
promedio = (calificación_1 + calificacion_2 + calificacion_3) / 3

# JERARQUIA DE OPERADORES: 1 - pot, raíz , 2 - mult y div , 3 - suma y resta

# SALIDA DE DATOS
print("El promedio es =", round(promedio, 2))
